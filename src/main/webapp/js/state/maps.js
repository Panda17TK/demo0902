export function getMap(id) {
  if (id) { const m = MAPS.find((x) => x.id === id); if (m) return m; }
  return MAPS[0];
}

// ランダムなマップ ID を返す。avoid を渡すと（可能なら）連続重複を避ける。
export function randomMapId(avoid) {
  if (MAPS.length <= 1) return MAPS[0].id;
  let pool = MAPS;
  if (avoid) { const filtered = MAPS.filter((m) => m.id !== avoid); if (filtered.length) pool = filtered; }
  return pool[Math.floor(Math.random() * pool.length)].id;
}
